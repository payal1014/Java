package Casting;

public class PriExplicitCast {
    public static void main(String[] args){
        double no = 14;
        float floatno = (float) no;
        System.out.println(no);

        float no1 = 20;
        long longno = (long) no1;
        System.out.println(no1);

        long no2 = 21;
        int intNo = (int) no2;
        System.out.println(no2);

        int no3 = 22;
        short shortNo = (short) no3;
        System.out.println(no3);

        short no4 = 23;
        byte byteNo = (byte) no4;
        System.out.println(no4);


    }
}
