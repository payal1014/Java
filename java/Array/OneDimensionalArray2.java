package Array;

public class OneDimensionalArray2 {
    public static void main(String[] args){
        String city[] = {"Akola","Nagpur","Amravati"};
        System.out.println("Second city name is " + city[1]);

        char alph[] = {'a','b','c','d','e','f'};
        System.out.println(alph[3]);

        int no[] = new int[4];
        no[0] = 1;
        no[1] = 2;
        no[2] = 3;
        no[3] = 4;
        System.out.println("First no is " + no[0]);
    }
}
