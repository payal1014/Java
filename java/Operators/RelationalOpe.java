package Operators;

public class RelationalOpe {
    public static void main(String[] args){
        // Is equal to relational-operator
        int no = 10;
        int no1 = 11;
        System.out.println(no==no1);
        //Not equal to relational operator
        int no2 = 12;
        int no3 = 13;
        System.out.println(no2!=no3);
        //Greater than relational operator
        int no4 = 15;
        int no5 = 14;
        System.out.println(no4>no5);
        //Smaller than relational operator
        int no6 = 16;
        int no7 = 17;
        System.out.println(no6<no7);
        //Grater than or Equal to
        int no8 = 18;
        int no9 = 19;
        System.out.println(no8>=no9);
        //Less than or Equal to
        int no10 = 20;
        int no11 = 20;
        System.out.println(no10<=no11);
    }
}
