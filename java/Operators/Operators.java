package Operators;

public class Operators {
    public static void main(String[] args){
        int no = 15;
        int no1 = 20;
        //addition operator
        System.out.println(no+no1);
        //subtraction operator
        System.out.println(no-no1);
        //multiplication operator
        System.out.println(no*no1);
        //division operator
        System.out.println(no/no1);
        //modulo operator
        System.out.println(no%no1);
        //Increment operator
        int num = 6;
        ++num;
        System.out.println(num);
        //decrement operator
        int num1 = 6;
        num1--;
        System.out.println(num1);

    }
}
